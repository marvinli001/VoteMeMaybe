export const buildRolePrompt = (
  role: Role,
  options: { wolfTeamList?: string[] } = {},
) => {
  const template = ROLE_PROMPTS[role] ?? "";
  if (role !== "werewolf") {
    return template;
  }
  const team = options.wolfTeamList?.length
    ? options.wolfTeamList.join("、")
    : "未知";
  return template.replace("{WOLF_TEAM_LIST}", team);
};

export const buildPlayerSystemPrompt = () =>
  [BASE_PLAYER_SYSTEM_PROMPT, OUTPUT_CONSTRAINT, OUTPUT_SCHEMA]
    .filter(Boolean)
